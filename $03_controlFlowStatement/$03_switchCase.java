package $03_controlFlowStatement;

public class $03_switchCase {
    public static void main(String[] args){
        String isItalphabet = "A";
        switch (isItalphabet.toLowerCase()){
            case "a":
                  System.out.println("this is alphabet");
                  break;

           case "e":
           System.out.println("this is alphabet");
           break;

           case "i":
                  System.out.println("this is alphabet");
                  break;

           case "o":
           System.out.println("this is alphabet");
           break;

           case "u":
           System.out.println("this is alphabet");
           break;

           default:
           System.out.println("error");
        }
        
    }

}
